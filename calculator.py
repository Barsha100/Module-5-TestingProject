"""calculator.py - simple functions for Module 5 testing assignment."""

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def concat_as_strings(a, b):
    return str(a) + str(b)

def swap_values(a, b):
    return b, a
